import * as React from "react"
import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card flex flex-col overflow-hidden rounded bg-card text-sm text-card-foreground border border-border shadow-sm [--card-spacing:--spacing(3)] data-[size=sm]:[--card-spacing:--spacing(2)]",
        className
      )}
      {...props}
    />
  )
}

function CardHeader({ className, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="card-header"
      className={cn(
        "flex items-center gap-2 px-3 py-2 text-white text-sm font-semibold",
        className
      )}
      style={{ background: "oklch(0.42 0.14 292)" }}
      {...props}
    />
  )
}

